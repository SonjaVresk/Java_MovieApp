    private void init() {
        try {
            initValidation();
            hideErrors();
            initRepository();
            initTable();
        } catch (Exception ex) {
            Logger.getLogger(DirectorPanel.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Unrecoverable error", "Cannot initiate the form");
            System.exit(1);
        }
    }

    private void initValidation() {
        validationFields = Arrays.asList(
                tfName
        );
        errorLabels = Arrays.asList(
                lbNameError
        );
    }

    private void hideErrors() {
        errorLabels.forEach(e -> e.setVisible(false));
    }

    private boolean formValid() {
        hideErrors();
        boolean ok = true;

        for (int i = 0; i < validationFields.size(); i++) {
            ok &= !validationFields.get(i).getText().trim().isEmpty();
            errorLabels.get(i).setVisible(validationFields.get(i).getText().trim().isEmpty());
        }
        return ok;
    }

    private void clearForm() {
        hideErrors();
        validationFields.forEach(e -> e.setText(""));
        selectedPerson = null;
    }

    private void initRepository() {
        repository = RepositoryFactory.getRepository(Person.class);
    }

    private void initTable() throws Exception {
        instPerson = new Person();
        instPerson.setJobID(JOB_ID);

        tblDirector.setRowHeight(25);
        tblDirector.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tblDirector.setAutoCreateRowSorter(true);

        personTableModel = new PersonTableModel(
                repository.selectAll(instPerson));
        tblDirector.setModel(personTableModel);
    }

    private void showDirector() {
        instPerson = new Person();
        instPerson.setJobID(JOB_ID);
        clearForm();
        int selectedRow = tblDirector.getSelectedRow();
        int rowIndex = tblDirector
                .convertRowIndexToModel(selectedRow);
        int id = (int) personTableModel.getValueAt(rowIndex, 0);

        try {
            Optional<Person> opt = (Optional<Person>) repository.select(id, instPerson);
            if (opt.isPresent()) {
                selectedPerson = opt.get();
                fill(selectedPerson);
            }
        } catch (Exception exception) {
        }
    }

    private void fill(Person person) {
        tfName.setText(person.getName());
    }
