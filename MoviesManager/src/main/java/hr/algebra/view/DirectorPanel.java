    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (selectedPerson == null) {
            MessageUtils.showInformationMessage("Info", "Director not selected!");
            return;
        }
        if (!formValid()) {
            return;
        }
        try {
            selectedPerson.setJobID(JOB_ID);
            selectedPerson.setName(tfName.getText().trim());

            repository.update(selectedPerson.getId(), selectedPerson);
            personTableModel.setPersons(repository.selectAll(selectedPerson));
            clearForm();
        } catch (Exception ex) {
            Logger.getLogger(DirectorPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (selectedPerson == null) {
            MessageUtils.showInformationMessage("Info", "Director not selected!");
            return;
        }
        if (MessageUtils.showConfirmDialog("Delete", "Delete director?")) {
            try {
                selectedPerson.setJobID(JOB_ID);
                repository.delete(selectedPerson.getId(), selectedPerson);
                personTableModel.setPersons(repository.selectAll(selectedPerson));
                clearForm();
            } catch (Exception ex) {
                Logger.getLogger(DirectorPanel.class.getName()).log(Level.SEVERE, null, ex);
                MessageUtils.showErrorMessage("Error", "Unable to delete director!");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


